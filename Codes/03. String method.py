name = "Abhishek Chaudhary"

#Upper method
print(name.upper())

#Lower method
print(name.lower())

#Strip method
print(name.strip())

#rstrip method
str = "Hello !!!"
print(str.rstrip())

#replace method
print(str.replace("!!!","Sir"))

#split method
print(str.split())

#Capitalize method
print(name.capitalize())

