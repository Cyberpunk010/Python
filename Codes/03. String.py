#Creating a String
name = "Abhishek"
print(name+" Chaudhary")

#Loop through an string
for char in name:
    print(char)

#Indexing
print(name[2])

#Len function in string
print(len(name))

#String Slicing
print(name[:2])  #Slicing from start
print(name[3:])  #slicing from end
print(name[1:7]) #slicing from between
print(name[:-3]) #slicing negative index
